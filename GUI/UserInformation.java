    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Pattern num = Pattern.compile("[0-9]{1,2}");
        Pattern let = Pattern.compile("[(a-zA-Z)|(a-zA-Z\\sa-zA-Z)]{2,30}");

        String usuario_Matricula = usuarioMatricula.getText();

        String usuario_nombre = nombre.getText();
        Matcher nomMatcher = let.matcher(usuario_nombre);

        String usuario_apellidoP = apellidoP.getText();
        Matcher apePMatcher = let.matcher(usuario_apellidoP);

        String usuario_apellidoM = apellidoM.getText();
        Matcher apeMMatcher = let.matcher(usuario_apellidoM);

        String usuario_edad = edad.getText();
        Matcher edadMatcher = num.matcher(usuario_edad);

        String usuario_aeronave = aeronaveMatricula.getText();

        if (usuario_Matricula.equals("")
                || usuario_nombre.equals("")
                || usuario_apellidoP.equals("")
                || usuario_apellidoM.equals("")
                || usuario_edad.equals("")
                || usuario_aeronave.equals("")
                || !edadMatcher.matches()
                || !nomMatcher.matches()
                || !apePMatcher.matches()
                || !apeMMatcher.matches()) {
            JOptionPane.showMessageDialog(null, "Verifiquie los campos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            FileWriter fichero = null;
            PrintWriter pw = null;
            try {
                String currentPath = new java.io.File(".").getCanonicalPath();
                fichero = new FileWriter(currentPath + "\\build\\classes\\Archivos\\Datos\\informacionPersonal.txt");
                pw = new PrintWriter(fichero);

                pw.println(usuario_Matricula);
                pw.println(usuario_nombre);
                pw.println(usuario_apellidoP);
                pw.println(usuario_apellidoM);
                pw.println(usuario_edad);
                pw.println(usuario_aeronave);
                pw.println("1");
                this.setVisible(false);

            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    // Nuevamente aprovechamos el finally para 
                    // asegurarnos que se cierra el fichero.
                    if (null != fichero) {
                        fichero.close();
                    }
                } catch (Exception e2) {
                    e2.printStackTrace();
                }
            }

            // instance of this application
            Aeronave_GUI aeronave = null;
            try {
                aeronave = new Aeronave_GUI();
            } catch (Exception ex) {
                Logger.getLogger(UserInformation.class.getName()).log(Level.SEVERE, null, ex);
            }

            // create the UI, including the map, for the application.
            JFrame appWindow = aeronave.createWindow();
            appWindow.add(aeronave.createUI());
            appWindow.setExtendedState(JFrame.MAXIMIZED_BOTH);
            appWindow.setVisible(true);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

